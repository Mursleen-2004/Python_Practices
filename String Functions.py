# lower: All characters convert in lower-case.
# upper: All characters convert in upper-case.
# title:  All characters convert in title-case.  (har word ka pehla letter capital.)
# capatalize:  All characters convert in capatalize-case.  (sirf jo start wala character hoga osy uuper-case mein kr dega.)

a = "my name is mursleen bukhari.";
b = a.upper()
print(b);

c = a.lower();
print(c);              # this functions lower,upper,title and capatalize takes no argument/parameter.


d = a.title()
print(d);

e = a.capitalize()
print(e);

